package de.be.thaw.text.model.element;

/**
 * Possible text element types.
 */
public enum TextElementType {

    /**
     * Text element containing text (may be emphasised: bold, italic, ...).
     */
    DEFAULT,

    /**
     * Code text element.
     */
    CODE,

    /**
     * A thingy text element.
     */
    THINGY

}
