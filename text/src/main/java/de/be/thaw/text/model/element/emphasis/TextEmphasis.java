package de.be.thaw.text.model.element.emphasis;

/**
 * Possible emphasis for text.
 */
public enum TextEmphasis {
    /**
     * The text is bold.
     */
    BOLD,

    /**
     * The text is italic.
     */
    ITALIC,

    /**
     * Underline the text.
     */
    UNDERLINED,

    /**
     * Code-like formatted
     */
    CODE
}
