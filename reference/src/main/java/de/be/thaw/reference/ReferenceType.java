package de.be.thaw.reference;

/**
 * Types of references.
 */
public enum ReferenceType {

    /**
     * Internal reference (to a part of the document).
     */
    INTERNAL,

    /**
     * External reference (e. g. website, ...).
     */
    EXTERNAL,

    /**
     * Reference to literature that has been predefined.
     */
    LITERATURE

}
